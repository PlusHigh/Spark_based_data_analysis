package com.qdu.model;

public class years {
    private String year;
    private int Name;

    public years() {
    }

    public years(String year, int name) {
        this.year = year;
        this.Name = name;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public int getName() {
        return Name;
    }

    public void setName(int name) {
        this.Name = name;
    }
}
